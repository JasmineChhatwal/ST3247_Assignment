def compute_summary(inf_vec, rew_vec, deg_hist_vec):
    """
    Compute a fixed-length summary-statistic vector from one simulation
    (or from the observed data).

    Statistics chosen to capture different aspects of the epidemic:
      0  peak_inf    — maximum infected fraction (magnitude of outbreak)
      1  time_peak   — time step at which peak occurs (speed of outbreak)
      2  final_inf   — fraction still infected at t=T (residual burden)
      3  auc         — area under infected curve (total exposure)
      4  total_rew   — cumulative rewiring events (avoidance behavior)
      5  mean_deg    — mean final degree (network structure change)

    These six statistics together allow ABC to distinguish scenarios where,
    e.g., a low beta combined with high rho produces a similar peak_inf
    as a high beta with low rho, but different time_peak and total_rew.
    """
    peak_inf  = np.max(inf_vec)
    time_peak = np.argmax(inf_vec).astype(float)
    final_inf = inf_vec[-1]
    auc       = np.sum(inf_vec)
    total_rew = float(np.sum(rew_vec))

    degrees  = np.arange(len(deg_hist_vec))
    mean_deg = np.sum(degrees * deg_hist_vec) / max(np.sum(deg_hist_vec), 1.0)


    return np.array([peak_inf, time_peak, final_inf, auc, total_rew, mean_deg])

