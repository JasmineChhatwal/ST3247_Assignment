def objective(params):
    """
    BO passes params as a list: [beta, gamma, rho].
    Returns the distance (to be MINIMIZED).
    """
    beta, gamma, rho = params

    # Run one simulation
    sim_inf, sim_rew, sim_deg = simulate(beta, gamma, rho)
    sim_sum = compute_summary_2(sim_inf, sim_rew, sim_deg)

    # Calculate standardized distance

    diff = (sim_sum - obs_summary_2) / stat_scales_2
    dist = np.sqrt(np.sum(diff**2))

    return dist

