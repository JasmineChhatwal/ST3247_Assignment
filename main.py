def abc_worker(seed):
    """
    Draw one parameter sample from the prior, simulate, and return
    [beta, gamma, rho, s0, s1, s2, s3, s4, s5] where s_i are summary stats.

    This function is called inside joblib's Parallel; the Numba-compiled
    _simulate_numba is already compiled when we reach this point, so there
    is no per-worker compilation overhead.
    """
    rng  = np.random.default_rng(seed)

    beta  = rng.uniform(0.05, 0.50)
    gamma = rng.uniform(0.02, 0.20)
    rho   = rng.uniform(0.00, 0.80)

    sim_inf, sim_rew, sim_deg = simulate(beta, gamma, rho, rng=rng)
    sim_sum = compute_summary(sim_inf, sim_rew, sim_deg)

    return [beta, gamma, rho, *sim_sum]

