## DEFINE THE SIMULATOR FUNCTION
"""
Adaptive-network SIR epidemic simulator — Numba-accelerated version.

Key design changes for Numba compatibility
------------------------------------------
The original code used Python sets for the adjacency list (neighbors[i]).
Numba's @njit cannot work with Python sets or variable-length lists of sets.
We therefore re-represent the graph as a DENSE adjacency matrix:

    adj : np.ndarray, shape (N, N), dtype np.bool_
    adj[i, j] == True  ⟺  edge (i, j) exists

Memory cost: 200×200×1 byte = 40 KB — negligible.
Lookup cost: O(1) (array index vs. hash table lookup in a set, but with no
             Python interpreter overhead inside the JIT-compiled function).

Degree bookkeeping: we maintain a separate `degree` array (shape N, int32)
to avoid calling np.sum(adj[i]) repeatedly inside the hot loop.

Random numbers: Numba supports numpy.random functions natively inside @njit;
we use numpy.random.random(), numpy.random.randint() etc.  The outer caller
sets np.random.seed(seed) before each call so results are reproducible.

Reference: Gross et al. (2006), PRL 96, 208701.
"""


# ── Core simulation (JIT-compiled, no Python objects) ───────────────────────────
@njit(cache=True)
def _simulate_numba(beta, gamma, rho, N, p_edge, n_infected0, T, seed):
    """
    JIT-compiled inner loop for the adaptive-network SIR model.

    Uses a dense boolean adjacency matrix instead of Python sets so that
    Numba can compile the function to native machine code.

    Parameters
    ----------
    beta, gamma, rho : float
        Transmission, recovery, and rewiring probabilities.
    N : int
        Population size (number of nodes).
    p_edge : float
        Initial Erdos-Renyi edge probability.
    n_infected0 : int
        Number of initially infected nodes.
    T : int
        Number of time steps.
    seed : int
        Seed for numpy.random (set inside the JIT function for thread safety).

    Returns
    -------
    infected_fraction : float64 array, shape (T+1,)
    rewire_counts     : int64   array, shape (T+1,)
    degree_histogram  : int64   array, shape (31,)
    """
    np.random.seed(seed)

    # ── Adjacency matrix & degree array ─────────────────────────────────────
    # adj[i, j] = True means an edge exists between i and j (undirected).
    # degree[i] caches len(neighbors[i]) to avoid recomputing it each step.
    adj    = np.zeros((N, N), dtype=np.bool_)
    degree = np.zeros(N, dtype=np.int32)

    # Build Erdos-Renyi graph
    for i in range(N):
        for j in range(i + 1, N):
            if np.random.random() < p_edge:
                adj[i, j] = True
                adj[j, i] = True
                degree[i] += 1
                degree[j] += 1

    # ── Initial states ───────────────────────────────────────────────────────
    # 0 = Susceptible, 1 = Infected, 2 = Recovered
    state = np.zeros(N, dtype=np.int8)

    # Sample n_infected0 distinct initial seeds via Fisher-Yates partial shuffle
    perm = np.arange(N)
    for k in range(n_infected0):
        r = k + np.random.randint(0, N - k)
        perm[k], perm[r] = perm[r], perm[k]
        state[perm[k]] = 1

    # ── Output arrays ────────────────────────────────────────────────────────
    infected_fraction = np.zeros(T + 1, dtype=np.float64)
    rewire_counts     = np.zeros(T + 1, dtype=np.int64)

    # Count initial infected fraction
    init_inf = 0
    for i in range(N):
        if state[i] == 1:
            init_inf += 1
    infected_fraction[0] = init_inf / N

    # ── Main loop ────────────────────────────────────────────────────────────
    # Temporary arrays reused each step to avoid heap allocations.
    # We over-allocate to the worst-case size; actual length tracked separately.
    new_inf_buf  = np.empty(N,      dtype=np.int32)   # newly infected nodes
    si_s_buf     = np.empty(N * 30, dtype=np.int32)   # S endpoint of S-I edges
    si_i_buf     = np.empty(N * 30, dtype=np.int32)   # I endpoint of S-I edges
    cand_buf     = np.empty(N,      dtype=np.int32)   # rewiring candidates

    for t in range(1, T + 1):

        # ── Phase 1: Infection (synchronous) ────────────────────────────────
        n_new = 0
        for i in range(N):
            if state[i] == 1:                          # i is infected
                for j in range(N):
                    if adj[i, j] and state[j] == 0:    # S-I edge
                        if np.random.random() < beta:
                            # Stage the infection; do not mutate state yet
                            new_inf_buf[n_new] = j
                            n_new += 1

        # Apply staged infections (may include duplicates — that is fine;
        # setting state to 1 twice is idempotent).
        for k in range(n_new):
            state[new_inf_buf[k]] = 1

        # ── Phase 2: Recovery ────────────────────────────────────────────────
        for i in range(N):
            if state[i] == 1:
                if np.random.random() < gamma:
                    state[i] = 2

        # ── Phase 3: Rewiring ────────────────────────────────────────────────
        # Collect all current S-I edges first (snapshot before any rewiring).
        n_si = 0
        for i in range(N):
            if state[i] == 0:                          # i is susceptible
                for j in range(N):
                    if adj[i, j] and state[j] == 1:    # j is infected
                        si_s_buf[n_si] = i
                        si_i_buf[n_si] = j
                        n_si += 1

        rewire_count = 0
        for k in range(n_si):
            s_node = si_s_buf[k]
            i_node = si_i_buf[k]

            if np.random.random() >= rho:
                continue                               # edge not rewired

            # Edge may have been removed by an earlier rewiring this step
            if not adj[s_node, i_node]:
                continue

            # Remove the S-I edge
            adj[s_node, i_node] = False
            adj[i_node, s_node] = False
            degree[s_node] -= 1
            degree[i_node] -= 1

            # Build candidate list: all nodes not s_node and not already adjacent
            n_cand = 0
            for c in range(N):
                if c != s_node and not adj[s_node, c]:
                    cand_buf[n_cand] = c
                    n_cand += 1

            if n_cand > 0:
                new_partner = cand_buf[np.random.randint(0, n_cand)]
                adj[s_node, new_partner] = True
                adj[new_partner, s_node] = True
                degree[s_node]       += 1
                degree[new_partner]  += 1
                rewire_count         += 1

        # ── Record statistics ────────────────────────────────────────────────
        n_inf = 0
        for i in range(N):
            if state[i] == 1:
                n_inf += 1
        infected_fraction[t] = n_inf / N
        rewire_counts[t]     = rewire_count

    # ── Final degree histogram ────────────────────────────────────────────────
    degree_histogram = np.zeros(31, dtype=np.int64)
    for i in range(N):
        d = degree[i]
        if d >= 30:
            degree_histogram[30] += 1
        else:
            degree_histogram[d]  += 1

    return infected_fraction, rewire_counts, degree_histogram


# ── Python wrapper (matches original API exactly) ───────────────────────────────
def simulate(beta, gamma, rho, N=200, p_edge=0.05, n_infected0=5, T=200, rng=None):
    """
    Run one replicate of the adaptive-network SIR model.

    This wrapper preserves the original function signature so that all
    downstream code (ABC workers, plotting, etc.) works without changes.
    Internally it calls _simulate_numba(), the JIT-compiled inner function.

    The `rng` argument is accepted for API compatibility but is not used;
    thread-safe seeding is handled inside the JIT function instead.

    Parameters
    ----------
    beta : float in [0, 1]     Transmission probability per S-I edge per step.
    gamma : float in [0, 1]    Recovery probability per infected node per step.
    rho : float in [0, 1]      Rewiring probability per S-I edge per step.
    N : int                    Number of nodes (default 200).
    p_edge : float             Erdos-Renyi edge probability (default 0.05).
    n_infected0 : int          Initially infected nodes (default 5).
    T : int                    Simulation length in steps (default 200).
    rng : ignored              Kept for backward compatibility only.

    Returns
    -------
    infected_fraction : np.ndarray, shape (T+1,)   Fraction infected at each step.
    rewire_counts     : np.ndarray, shape (T+1,)   Rewiring events at each step.
    degree_histogram  : np.ndarray, shape (31,)    Final degree histogram.
    """
    # Draw a 32-bit seed from numpy's global RNG so that passing rng=... from
    # the outer loop still propagates randomness (the outer rng object is not
    # used directly, but calling rng.integers ensures it advances correctly).
    if rng is not None:
        seed = int(rng.integers(0, 2**31))
    else:
        seed = int(np.random.randint(0, 2**31))
    return _simulate_numba(beta, gamma, rho, N, p_edge, n_infected0, T, seed)


