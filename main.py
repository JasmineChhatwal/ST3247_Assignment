def apply_regression_adjustment_2(accepted_params, accepted_summaries, obs_summary):
    """
    Adjusts the accepted parameters from Run 2 using local linear regression.
    """
    adjusted_params = np.zeros_like(accepted_params)

    # Perform a separate regression for each parameter (Beta, Gamma, Rho)
    for i in range(accepted_params.shape[1]):
        # Features: The difference between simulated stats and observed stats
        X = accepted_summaries - obs_summary
        y = accepted_params[:, i]

        # Fit the model
        model = LinearRegression().fit(X, y)

        # The adjustment:
        # We find what the model predicts the parameter should be when X = 0 (perfect match)
        # and then add the residuals (the part the model couldn't explain).
        prediction_at_target = model.predict(np.zeros((1, X.shape[1])))
        residuals = y - model.predict(X)
        adjusted_params[:, i] = prediction_at_target + residuals

    # Safety: Clip adjusted values to stay within the original prior bounds
    # beta: [0.05, 0.50], gamma: [0.02, 0.20], rho: [0.0, 0.8]
    adjusted_params[:, 0] = np.clip(adjusted_params[:, 0], 0.05, 0.50)
    adjusted_params[:, 1] = np.clip(adjusted_params[:, 1], 0.02, 0.20)
    adjusted_params[:, 2] = np.clip(adjusted_params[:, 2], 0.00, 0.80)

    return adjusted_params

